void solve() {
    int n;
    cin >> n;
    map<int, int> mp;
    vector<int> a(n + 1), b(n + 1);
    for (int i = 1; i <= n; i++) cin >> a[i];
    for (int i = 1; i <= n; i++) {
        if (!mp[a[i]])
            b[i] = -1;
        else
            b[i] = mp[a[i]];
        mp[a[i]] = i;
    }
    for (int i = 1; i <= n; i++) cout << b[i] << ' ';
}

signed main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);
    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
